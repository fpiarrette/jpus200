package com.tas.es.obsw.utils.net.jpus.model.service200.processor;

public class LsCpuException extends Exception {

	private static final long serialVersionUID = 1L;

	public LsCpuException(String message) {
		super(message);
	}

	public LsCpuException(Throwable cause) {
		super(cause);
	}

	public LsCpuException(String message, Throwable cause) {
		super(message, cause);
	}

	public LsCpuException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
